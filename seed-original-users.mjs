async function seedUsers() {
  try {
    console.log('🌱 Seeding original users data...\n');

    // Clear existing users first
    const deletedCount = await prisma.users.deleteMany({});
    console.log(`✓ Cleared ${deletedCount.count} existing users\n`);

    // Insert original users
    for (const user of originalUsers) {
      const result = await prisma.users.upsert({
        where: { id: user.id },
        update: {
          name: user.name,
          email: user.email,
          role: user.role,
          department: user.department,
          position: user.position,
          phone: user.phone,
          hourlyRate: user.hourly_rate ? parseFloat(user.hourly_rate) : 0,
          weeklyCapacity: user.weekly_capacity ? parseFloat(user.weekly_capacity) : 40,
          name_th: user.name_th,
          employee_code: user.employee_code,
          status: user.status,
          avatar: user.avatar,
          is_active: user.is_active,
          is_deleted: user.is_deleted,
          failed_login_attempts: user.failed_login_attempts,
        },
        create: {
          id: user.id,
          name: user.name,
          email: user.email,
          role: user.role,
          department: user.department,
          position: user.position,
          phone: user.phone,
          hourlyRate: user.hourly_rate ? parseFloat(user.hourly_rate) : 0,
          weeklyCapacity: user.weekly_capacity ? parseFloat(user.weekly_capacity) : 40,
          password: user.password,
          username: user.email.split('@')[0],
          name_th: user.name_th,
          employee_code: user.employee_code,
          status: user.status,
          avatar: user.avatar,
          is_active: user.is_active,
          is_deleted: user.is_deleted,
          failed_login_attempts: user.failed_login_attempts,
        },
      });
      console.log(`✓ ${result.name} (${result.email})`);
    }

    console.log(`\n✅ Seeded ${originalUsers.length} users successfully!\n`);
    return true;

  } catch (error) {
    console.error('❌ Error seeding users:', error.message);
    return false;
  } finally {
    await prisma.$disconnect();
  }
}

